package com.example.biitalumnidirectory.Friends;

public class Friend {
    public String Friend_CNIC;
    public String Arid_No;
    public String Name;
    public String Dicipline;
    public String Section;
    public String Semester;
    public String NewImage;
    public String OldImage;


    public Friend(String friend_CNIC, String arid_No, String name, String dicipline, String section, String semester, String newImage, String oldImage) {
        Friend_CNIC = friend_CNIC;
        Arid_No = arid_No;
        Name = name;
        Dicipline = dicipline;
        Section = section;
        Semester = semester;
        NewImage = newImage;
        OldImage = oldImage;
    }

}
