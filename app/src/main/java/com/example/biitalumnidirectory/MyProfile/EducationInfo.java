package com.example.biitalumnidirectory.MyProfile;


public class EducationInfo {

    public String id;
    public String Degree_name;
    public String Instituation_name;
    public String Completion_year;
    public String Major_Subject;


    public EducationInfo(String id, String degree_name, String instituation_name, String completion_year, String major_Subject) {
        this.id = id;
        Degree_name = degree_name;
        Instituation_name = instituation_name;
        Completion_year = completion_year;
        Major_Subject = major_Subject;
    }
}

