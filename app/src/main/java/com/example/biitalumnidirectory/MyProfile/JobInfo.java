package com.example.biitalumnidirectory.MyProfile;

public class JobInfo {

    public String id;
    public String Designation;
    public String Organization;
    public String Starting_Date;
    public String Ending_Date;

    public JobInfo(String id, String designation, String organization, String starting_Date, String ending_Date) {
        this.id = id;
        Designation = designation;
        Organization = organization;
        Starting_Date = starting_Date;
        Ending_Date = ending_Date;
    }
}
