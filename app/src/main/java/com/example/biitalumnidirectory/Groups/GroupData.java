package com.example.biitalumnidirectory.Groups;

public class GroupData {
    public String SJE_Detail_Id;
    public String Student_Name;
    public String Posted_Date;
    public String Posted_Time;
    public String Title;
    public String Type;

    public GroupData(String SJE_Detail_Id, String student_Name, String posted_Date, String posted_Time, String title, String type) {
        this.SJE_Detail_Id = SJE_Detail_Id;
        Student_Name = student_Name;
        Posted_Date = posted_Date;
        Posted_Time = posted_Time;
        Title = title;
        Type = type;
    }
}
